package com.java.basics;

public class IntegerRotation {

	public static void main(String[] args) {

		
//		byte short int long 
		
		int x = 2147483647;// Maximum +ve
		x += 5;
		System.out.println(x);// 2147483648

//	int	[-2147483648 to   0  to  +2147483647]

		x = -2147483647;// Maximum +ve
		x -= 5;
		System.out.println(x);// 9006567388
		
//		2s complement

	}

}
